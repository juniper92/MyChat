import SwiftUI

extension Color {
    
    struct Palette {
        static var Background: Color { 
            return Color("background")
        }
        
        static var ButtonPrimary: Color {
            return Color("button-primary")
        }
        
        static var IconsPrimary: Color {
            return Color("icons-primary")
        }
        
        static var IconsSecondary: Color {
            return Color("icons-secondary")
        }
        
        static var TextButton: Color {
            return Color("text-button")
        }
        
        static var TextInput: Color {
            return Color("text-input")
        }
        
        static var TextBackground: Color {
            return Color("text-background")
        }
    }
}
